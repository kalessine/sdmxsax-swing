    DataMessage msg = null;
    
    public void setFile(File file) {
        FileInputStream fis = null;
        try{
            if( file == null ) return;
            fis = new FileInputStream(file);
            msg = SdmxIO.parseData(fis, false);
            DataMessageTableModel model = new DataMessageTableModel();
            model.setDataMessage(msg);
            jTable1.setModel(model);
            MainJFrame.FRAME.setVisible(false);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fis.close();
            } catch (IOException ex) {
                Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }
    
