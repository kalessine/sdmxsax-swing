    private void jmSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmSaveActionPerformed
        FileOutputStream fos = null;
        try {
            JFileChooser jfc = new JFileChooser();
            jfc.showSaveDialog(this);
            File f = jfc.getSelectedFile();
            if( f == null ) return;
            fos = new FileOutputStream(f);
            CompactDataWriter.write(msg, fos);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (XMLStreamException ex) {
            Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if(fos!=null)fos.close();
            } catch (IOException ex) {
                Logger.getLogger(DisplayDataMessageJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jmSaveActionPerformed

